#include <stdio.h>
/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition final answer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtraction final answer
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: multiplication final answer
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: division results
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
